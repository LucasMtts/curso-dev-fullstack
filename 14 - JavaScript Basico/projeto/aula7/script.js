/*

adição: +
subtração: - 
multiplicação: *
divisão: /
incremento: ++
decremento: --


atribuição

*/


// adição
let a = 10
let b = 5
let c = a + b
console.log(c)


// subtração
let d = -10
let e = 5
let f = d - e
console.log(f)

// multiplicação
let g = 10
let h = 5
let i = g * h
console.log(i)

// divisão
let j = 12
let k = 6
let l = j / k
console.log(l)

// incremento
let m = 10
m++
console.log(m)

//decremento
let n = 10
n--
console.log(n)




// atribuição
let o = 12
o +=12  // 0 = o + 12
console.log(o)

let p = 12
p -=12  // 0 = p - 12
console.log(p)